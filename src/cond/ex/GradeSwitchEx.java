package cond.ex;

public class GradeSwitchEx {
    public static void main(String[] args) {
        String grade = "b";

        switch (grade){
            case "a":
                System.out.println("탁월한 성과입니다.");
                break;
            case "b":
                System.out.println("좋은 성과입니다.");
                break;
            case "c":
                System.out.println("준수 성과입니다.");
                break;
            case "d":
                System.out.println("향상 성과입니다.");
                break;
            case "e":
                System.out.println("노력 성과입니다.");
                break;
            case "f":
                System.out.println("탈락입니다..");
                break;
            default:
        }





    }
}
